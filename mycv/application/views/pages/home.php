		<p>
		</p><h3>What is the Hib vaccine?</h3>

		The Hib vaccine helps prevent Haemophilus influenzatype b, a 
		leading cause of serious illness in children. It can lead to meningitis,
		pneumonia and a severe throat infection. The Hib vaccine is given as a 
		series of 3 or 4 shots, from age 2 months to 15 months.
		<p></p>
		<p>
		</p><h3>What is the MMR vaccine?</h3>

		The MMR vaccine protects against the measles, mumps, and rubella 
		(MMR). It’s given as 2 shots when your child is 1 year old and again 
		when they are 4- to 6 years old.

		The measles cause fever, rash, cough, runny nose, and watery eyes. 
		It can also cause ear infections and pneumonia. Measles can also lead to
		more serious problems, such as brain swelling and even death.

		The mumps cause fever, headache, and painful swelling of one or 
		both of the major saliva glands. Mumps can lead to meningitis (infection
		of the coverings of the brain and spinal cord) and, very rarely, to 
		brain swelling. Rarely, it can cause the testicles of boys or men to 
		swell, which can make them unable to have children.

		Rubella is also called the German measles. It causes a slight 
		fever, a rash and swelling of the glands in the neck. Rubella can also 
		cause brain swelling or a problem with bleeding.

		If a pregnant woman catches rubella, it can cause her to lose her 
		baby or have a baby who is blind or deaf, or has trouble learning.

		Some people have suggested that the MMR vaccine causes autism. 
		However, research has shown that there is no link between autism and 
		childhood vaccinations.
		<p></p>
		<p></p><h3>What is the varicella vaccine?</h3>

		The varicella vaccine helps prevent chickenpox. It is given to 
		children once after they are 12 months old and again at 4- to 6 years 
		old, or to older children if they have never had chickenpox or been 
		vaccinated.<p></p>

