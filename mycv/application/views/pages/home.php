			<h3>
				Vaccines for Your Children: Protect Your Child at Every Age
			</h3>
			<p>
				Immunizations have had an enormous impact on improving the health 
				of children in the United States. Most parents today have never seen 
				first-hand the devastating consequences that vaccine-preventable 
				diseases have on a child, a family, or community. While these diseases 
				are not common in the U.S., they persist around the world. It is 
				important that we continue to protect our children with vaccines because
				outbreaks of vaccine-preventable diseases like pertussis, mumps, and 
				measles can and do occur in this country

				Vaccination is one of the best ways parents can protect infants, 
				children, and teens from 16 potentially harmful diseases. 
				Vaccine-preventable diseases can be very serious, may require 
				hospitalization, or even be deadly — especially in infants and young 
				children. 
			</p>
			<p>
			</p><h3>What is the DTaP vaccine?</h3>

			The DTaP vaccine is 3 vaccines in 1 shot. It protects against 
			diphtheria, tetanus and pertussis. It’s given as a series of 5 shots, 
			the first when your child is 2 months old and the last when they are 4- 
			to 6 years old.

			Diphtheria is a disease that attacks the throat and heart. It can 
			lead to heart failure and death. Tetanus is also called “lockjaw.” It 
			can lead to severe muscle spasms and death. Pertussis (also called 
			“whooping cough”) causes severe coughing that makes it hard to breathe, 
			eat, and drink. It can lead to pneumonia, convulsions, brain damage, and
			death.

			Having your child immunized when he or she is young (which means 
			making sure he or she gets all of the DTaP shots) protects your child 
			against these diseases for about 10 years. After this time, your child 
			will need booster shots.
			<p></p>
			<p>
			</p><h3>What is the rotavirus vaccine?</h3>

			The rotavirus vaccine protects against rotavirus. Your child will 
			receive either a two-dose, at 2 and 4 months of age, or a three-dose 
			series, at 2, 4, and 6 months of age, depending on what your doctor 
			recommends. All doses should be given by no later than age 8 months of 
			age.

			Rotavirus is a virus that causes diarrhea, mostly in babies and 
			young children. The diarrhea can be severe and cause dehydration. 
			Rotavirus can also cause vomiting and fever in babies.

			After rotavirus vaccination, call your family doctor if your child 
			has stomach pain with severe crying (which may be brief), vomiting, 
			blood in the stool, or is acting weak or very irritable. This is 
			especially important within the first seven days after rotavirus 
			vaccination. Contact your doctor if your child has any of these signs, 
			even if it has been several weeks since the last dose of vaccine.
			<p></p>
			<p>
			</p><h3>What is the IPV vaccine?</h3>

			The IPV (inactivated poliovirus) vaccine helps prevent polio. It’s 
			given four times as a shot, from age 2 months to 6 years.

			Polio can cause muscle pain and paralysis of one or both legs or 
			arms. It may also paralyze the muscles used to breathe and swallow. It 
			can lead to death.
			<p></p>
			<p></p><h3>What is the HBV vaccine?</h3>

			The HBV vaccine helps prevent hepatitis B virus (HBV) infection, an
			infection of the liver that can lead to liver cancer and death. The 
			vaccine is given as a series of 3 shots, with the first shot given soon 
			after birth.<p></p>


